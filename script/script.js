$(document).ready(function () {
    $(".doc").hide();
    $(".title").hover(function () {
        $(".doc").show();
    }, function () {
        $(".doc").hide();
    });
    $(".doc").hover(function () {
        $(".doc").show();
    }, function () {
        $(".doc").hide();
    });
});